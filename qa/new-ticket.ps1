Write-NewFile $planPath $plan
Write-NewFile $designPath $design
Write-NewFile $analysisPath $analysis
Write-NewFile $reportPath $report
Write-NewFile $feedbackPath $feedback

if (Test-Path $backlogPath) {
  $backlogLine = "| $Id | $Title | $Priority | generated by qa/new-ticket.ps1 |"
  $backlogContent = Get-Content -Path $backlogPath -Raw
  if (-not $backlogContent.Contains("| $Id |")) {
    $sourceClusterMarker = "`r`n## Source Clusters"
    if (-not $backlogContent.Contains($sourceClusterMarker)) {
      $sourceClusterMarker = "`n## Source Clusters"
    }

    if ($backlogContent.Contains($sourceClusterMarker)) {
      $updatedBacklog = $backlogContent.Replace($sourceClusterMarker, "`r`n$backlogLine`r`n## Source Clusters")
      Set-Content -Path $backlogPath -Value $updatedBacklog -Encoding UTF8
    } else {
      Add-Content -Path $backlogPath -Value $backlogLine -Encoding UTF8
    }
  }
}

Write-Output "NEW_TICKET_CREATED $Id $baseName"
